-- Ex1. 부서번호를 받아 부서정보를 출력
SELECT *FROM DEPT WHERE DEPTNO=10;
INSERT INTO DEPT VALUES (50,'IT','서울');
SELECT*FROM DEPT;
COMMIT;
DELETE FROM DEPT WHERE DEPTNO=50;
COMMIT;
-- EX2. 부서번호 입력 받아 부서 정보와 사원 정보 출력
SELECT*FROM DEPT WHERE DEPTNO=10;
SELECT W.EMPNO,W.ENAME,W.SAL,M.ENAME MNAME
    FROM EMP W,EMP M
    WHERE W.MGR=M.EMPNO AND W.DEPTNO=10;
-- EX3. 부서이름으로 부서정보 출력
SELECT*FROM DEPT WHERE UPPER(DNAME)=UPPER('sales');

